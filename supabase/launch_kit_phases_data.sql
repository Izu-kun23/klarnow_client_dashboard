-- Launch Kit Phase Definitions
-- Each project gets exactly 4 phases when initialized

-- Phase 1: Inputs & clarity (Days 0–2)
-- Subtitle: "Lock the message and plan."
-- Checklist items:
--   1. Onboarding steps completed
--   2. Brand / strategy call completed
--   3. Simple 14 day plan agreed
-- Next from us (examples):
--   - "We are taking your inputs and call notes and shaping the outline for your 3 pages."
-- Next from you (examples):
--   - "Nothing for now. We will come back to you with copy to review."

-- Phase 2: Words that sell (Days 3–5)
-- Subtitle: "We write your 3 pages."
-- Checklist items:
--   1. Draft homepage copy ready
--   2. Draft offer / services page ready
--   3. Draft contact/about copy ready
--   4. You reviewed and approved copy
-- Links (optional):
--   - View copy doc
-- Next from us (examples):
--   - "We are drafting your homepage and offer page copy."
-- Next from you (examples):
--   - "Please review your copy and leave one round of comments."

-- Phase 3: Design & build (Days 6–10)
-- Subtitle: "We turn copy into a 3 page site."
-- Checklist items:
--   1. Site layout built for all 3 pages
--   2. Mobile checks done
--   3. Testimonials and proof added
--   4. Staging link shared with you
-- Links (optional):
--   - View staging site
-- Next from us:
--   - "We are building your 3 page site using your approved copy."
-- Next from you:
--   - "Please click through the staging site and flag any small tweaks."

-- Phase 4: Test & launch (Days 11–14)
-- Subtitle: "We connect domain, test and go live."
-- Checklist items:
--   1. Forms tested
--   2. Domain connected
--   3. Final tweaks applied
--   4. Loom walkthrough recorded and shared
-- Links (optional):
--   - View live site
--   - Watch Loom walkthrough
-- Next from us:
--   - "We are testing your contact form and connecting your domain."
-- Next from you:
--   - "Please confirm everything looks good and share any final non-urgent tweaks."

