-- PHASE DATA SUMMARY
-- ============================================

-- Launch Kit Phase 1: Inputs & clarity
--   - Days: 0-2
--   - Subtitle: "Lock the message and plan."
--   - Checklist: 3 items
--   - No links

-- Launch Kit Phase 2: Words that sell
--   - Days: 3-5
--   - Subtitle: "We write your 3 pages."
--   - Checklist: 4 items
--   - Links: View copy doc (optional)

-- Launch Kit Phase 3: Design & build
--   - Days: 6-10
--   - Subtitle: "We turn copy into a 3 page site."
--   - Checklist: 4 items
--   - Links: View staging site (optional)

-- Launch Kit Phase 4: Test & launch
--   - Days: 11-14
--   - Subtitle: "We connect domain, test and go live."
--   - Checklist: 4 items
--   - Links: View live site, Watch Loom walkthrough (optional)

-- ============================================
-- NOTES
-- ============================================
-- - All phases start with status 'NOT_STARTED'
-- - Phase status is automatically updated by triggers in phases_triggers.sql
--   based on checklist item completion
-- - Phase links are stored in the phase_links table (defined in schema.sql)
--   and are optional - they can be added later by admins
-- - "Next from us" and "Next from you" text is stored at the project level
--   in the projects table (next_from_us, next_from_you columns)
-- - The initialize_launch_kit_project() function in schema.sql automatically
--   creates these phases and checklist items when a new Launch Kit project is created

